from Bio import SeqIO

def baseFiltQual(file,threshold):
	records = []
	for record in SeqIO.parse(file,'fastq'):		
		phred = record.letter_annotations['phred_quality']
		start,end = 0,len(phred)
		while phred[start] < threshold:
			start += 1
		while phred[end-1] < threshold:
			end -= 1
		print(record[start:end].format('fastq'),end='')
	
